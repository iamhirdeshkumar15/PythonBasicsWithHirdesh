# Assigning value 500 to variable 'b'
b = 500
# Calculating the sum of 'a' and 'b' and storing it in 'sum'
sum = a + b
# Calculating the difference between 'a' and 'b' and storing it in 'diff'
diff = a - b
# Printing the value of 'sum' (expected output: 1500)
print(sum)
# Printing the value of 'diff' (expected output: 500)
print(diff)

